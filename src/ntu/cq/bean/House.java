package ntu.cq.bean;

public class House {

	private int Hid;
	private int Bid;
	private String Haddr;
	private String Hstatus;
	public int getHid() {
		return Hid;
	}
	public void setHid(int hid) {
		Hid = hid;
	}
	public int getBid() {
		return Bid;
	}
	public void setBid(int bid) {
		Bid = bid;
	}
	public String getHaddr() {
		return Haddr;
	}
	public void setHaddr(String haddr) {
		Haddr = haddr;
	}
	public String getHstatus() {
		return Hstatus;
	}
	public void setHstatus(String string) {
		Hstatus = string;
	}
	
	
}
